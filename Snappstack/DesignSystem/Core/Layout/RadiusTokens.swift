import SwiftUI

struct RadiusTokens {
    /// 2pts - Micro radius for small elements
    static let micro: CGFloat = 2
    
    /// 4pts - Extra small radius
    static let xs: CGFloat = 4
    
    /// 8pts - Small radius
    static let sm: CGFloat = 8
    
    /// 12pts - Default radius
    static let md: CGFloat = 12
    
    /// 16pts - Large radius
    static let lg: CGFloat = 16
    
    /// 24pts - Extra large radius
    static let xl: CGFloat = 24
    
    /// 999pts - Full radius for circles
    static let full: CGFloat = 999
}
