import SwiftUI

public struct SpacingTokens {
    /// 1pts - Micro spacing for tight layouts
    public static let line: CGFloat = 1
    
    /// 2pts - Micro spacing for tight layouts
    public static let micro: CGFloat = 2
    
    /// 4pts - Minimum spacing between elements
    public static let xxxs: CGFloat = 4
    
    /// 8pts - Extra small spacing
    public static let xxs: CGFloat = 8
    
    /// 12pts - Small spacing
    public static let xs: CGFloat = 12
    
    /// 14pts - Small2 spacing
    public static let xs2: CGFloat = 14
    
    /// 16pts - Default spacing
    public static let sm: CGFloat = 16
    
    /// 20pts - Medium spacing
    public static let md: CGFloat = 20
    
    /// 24pts - Large spacing
    public static let lg: CGFloat = 24
    
    /// 32pts - Extra large spacing
    public static let xl: CGFloat = 32
    
    /// 40pts - Double extra large spacing
    public static let xxl: CGFloat = 40
    
    /// 48pts - Triple extra large spacing
    public static let xxxl: CGFloat = 48
    
    /// Screen edge margin
    public static let screenEdge: CGFloat = 16
    
    /// Tabbar height
    public static let tabbar: CGFloat = 49
}
