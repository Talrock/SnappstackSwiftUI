import SwiftUI

struct SpacingTokens {
    /// 2pts - Micro spacing for tight layouts
    static let micro: CGFloat = 2
    
    /// 4pts - Minimum spacing between elements
    static let xxxs: CGFloat = 4
    
    /// 8pts - Extra small spacing
    static let xxs: CGFloat = 8
    
    /// 12pts - Small spacing
    static let xs: CGFloat = 12
    
    /// 16pts - Default spacing
    static let sm: CGFloat = 16
    
    /// 20pts - Medium spacing
    static let md: CGFloat = 20
    
    /// 24pts - Large spacing
    static let lg: CGFloat = 24
    
    /// 32pts - Extra large spacing
    static let xl: CGFloat = 32
    
    /// 40pts - Double extra large spacing
    static let xxl: CGFloat = 40
    
    /// 48pts - Triple extra large spacing
    static let xxxl: CGFloat = 48
    
    /// Screen edge margin
    static let screenEdge: CGFloat = 16
}
